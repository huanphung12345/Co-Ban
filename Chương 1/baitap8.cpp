/* b3 in giá trị cho a */
#include <iostream>
using namespace std;
int main(){
    int a; // b1 khai báo biến
    cout << "Nhap gia tri a: ";
    cin  >> a; //b2 nhập giá trị cho biến
    cout << "gia tri cua a: ";
    cout << a; // b3 in ra màn hình
    return 0;
}